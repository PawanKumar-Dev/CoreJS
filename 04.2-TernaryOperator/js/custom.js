// Ternary Operator: It is the only operator which takes 3 operands in JS. It's short version of If-Else Statements.

// Syntax: varaible = (condition) ? value1 : value2
// example:
let age = 19;
let age2 = 16;

let canVote1 = (age>18)?"You can vote!":"You cannot vote! you are underage";
let canVote2 = (age2>18)?"You can vote!":"You cannot vote! you are underage";

console.log(canVote1);
console.log(canVote2);