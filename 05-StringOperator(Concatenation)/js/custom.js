// In JS, you use the + operator to concatenate(join) two or more strings.
// As we can see even white space wrapped in quotes is treated at string and hence concatenated

let fname = 'Pawan';
let lname = 'Lal';

console.log(fname + " " + lname);


// Note: When + is used with strings, it concatenates. However, when + is used with numbers, it does addition.