// Math Object provide us some key methods for mathematical operations in JS

// ===============================
// Math.PI() method : gives us the accurate PI value

console.log(Math.PI);                 // Output: 3.141592653589793

// Math.round() : round up our decimal numbers

console.log(Math.round(4.7));         // Output: 5


// Math.pow(x, y): calculate power of a number (x to power y)

console.log(Math.pow(7, 2));          // 7 to power 2: 49

// Math.abs(): return the absolute +ve value of given number

console.log(Math.abs(-456));          // Output: 456

// Math.min() and Math.max(): returns the min or max value in list of argument

console.log(Math.min(5, 46, 1, 78, 12));    // Output: 1
console.log(Math.max(5, 46, 1, 78, 12));    // Output: 78

// Math.random(x, y): returns a random number(0 <-> 1).
console.log(Math.random());